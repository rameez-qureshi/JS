
// ARITHMETIC OPERATORS
let a = 10;
let b = 4;
console.log("a + b = ", a + b)
console.log("a - b = ", a - b)
console.log("a / b = ", a / b)
console.log("a ** b = ", a ** b)
console.log("a % b = ", a % b)
console.log("a++ = ", a++)          // print then increament
console.log("++a = ", ++a)          // increament then print
console.log("a-- = ", a--)          // print then decreament
console.log("--a = ", --a)          // decreament then print
console.log("a = ", a)

// Assignment Operators 
let c = 1;
c += 5                              // same as c = c + 5
console.log("c is now = ", c)
c -= 5                              // same as c = c - 5
console.log("c is now = ", c)
c *= 5                              // same as c = c * 5
console.log("c is now = ", c)
c /= 5                              // same as c = c / 5
console.log("c is now = ", c)

// Comparison Operators
let c1 = 6;
let c2 = 6;
console.log("c1 == c2 is ", c1 == c2)      // 6 = "6"  ans is true.
console.log("c1 != c2 is ", c1 != c2)
console.log("c1 === c2 is ", c1 === c2)    // 6 = "6"  ans is false.
console.log("c1 !== c2 is ", c1 !== c2)
console.log("c1 > c2 is ", c1 > c2)        // < , <= , > , >=

// Logical Operators
let x = 5;
let y = 6;
console.log( x<y && x==5 )    // AND Operator // both condition true PRINT true otherwise false
console.log( x>y || x==5 )    // OR Operator  // anyone condition true PRINT true otherwise false
console.log( !false )         // Not Operator // print opposite    
console.log( !true )

